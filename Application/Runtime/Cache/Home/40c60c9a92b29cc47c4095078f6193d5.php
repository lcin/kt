<script type="text/javascript" charset="utf-8">
	var p;
	var q;
	$("input[name = 'username']").blur(function()
	{
		var val = this.value;
		//alert(val);
		if(!val){
			$("#sp1").html("你好像忘了点什么");
			p = false;
		}else{
			p = true;
		}
	});
	$("input[name = 'email']").blur(function()
	{
		var val = this.value;
		//alert(val);
		if(!val){
			$("#sp2").html("你好像忘了点什么");
			$("#sp2").css("color","red");
			q = false;
		}else{
			q = true;
		}
	});
	$("form").submit(function(){
		$("input[name = 'username']").blur();
		$("input[name = 'email']").blur();
		if(p && q){
			return true;
		}
		return false;
	});
